#Program to convert celcius into Fahrenheit and to find the Boiling and Freezing point of water

num1=float(input("Enter the Temperature in Celsius: "))

result1= (num1 * 9/5)+32 
print("Temperature in Fahrenheit is: ",result1)

print("Let Boiling Point of water be 100 celcius")

result2= (100 * 9/5)+32
print("Boiling point in Fahrenheit is: ",result2)

print("Let the Freezing point of water be 0 celcius")

result3= (0 * 9/5)+32
print("Freezing point in Fahrenheit is: ",result3)
